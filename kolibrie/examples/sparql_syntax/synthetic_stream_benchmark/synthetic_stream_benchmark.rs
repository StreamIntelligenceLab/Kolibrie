use kolibrie::execute_query::*;
use kolibrie::sparql_database::*;
use shared::index_manager::*;
use std::collections::HashMap;
use std::fs::File;
use std::io::{BufRead, BufReader};
use std::time::Instant;

fn load_queries(path: &str) -> HashMap<String, String> {
    let file = File::open(path).expect("Failed to open queries JSON file");
    let reader = BufReader::new(file);
    serde_json::from_reader(reader).expect("Failed to parse JSON queries")
}

fn make_config_from_env(queries: Vec<String>) -> (String, IndexConfig) {
    let index_type = std::env::var("INDEX_TYPE")
        .unwrap_or_else(|_| "hexastore".to_string())
        .to_lowercase();

    let config = match index_type.as_str() {
        "hexastore" | "" => IndexConfig::Hexastore,
        "spo" => IndexConfig::SPO,
        "pos" => IndexConfig::POS,
        "pso" => IndexConfig::PSO,
        "sop" => IndexConfig::SOP,
        "ops" => IndexConfig::OPS,
        "osp" => IndexConfig::OSP,
        "table" => IndexConfig::SingleTable,
        "partial_hexastore" => IndexConfig::PartialHexastore { queries },
        "buckets" => IndexConfig::Buckets { queries },
        other => {
            eprintln!("Unknown INDEX_TYPE '{}', falling back to hexastore.", other);
            IndexConfig::Hexastore
        }
    };

    (index_type, config)
}

fn main() {
    let window_size_str = std::env::var("WINDOW_SIZE").unwrap_or_else(|_| "50000".to_string());
    let window_size: usize = window_size_str.parse().unwrap();
    let slide_size_str = std::env::var("SLIDE_SIZE").unwrap_or_else(|_| "10000".to_string());
    let slide_size: usize = slide_size_str.parse().unwrap();

    // 1. Load queries generated by Python
    let query_map = load_queries("./benchmark_dataset/synthetic_queries.json");
    
    // Sort queries to run them in a consistent order (Q1, Q2, etc.)
    let mut query_keys: Vec<_> = query_map.keys().cloned().collect();
    query_keys.sort();

    // 2. Load all triples into memory once to avoid disk I/O bottlenecks during the benchmark
    let file = File::open("./benchmark_dataset/synthetic_1M.nt").expect("Run Python script first");
    let reader = BufReader::new(file);

    let mut all_triples = Vec::new();
    for line in reader.lines() {
        if let Ok(l) = line {
            if !l.trim().is_empty() {
                all_triples.push(l);
            }
        }
    }
    println!("Loaded {} triples from disk.", all_triples.len());

    // 3. Iterate over each query individually
    for query_id in query_keys {
        let query_string = query_map.get(&query_id).unwrap().clone();
        
        // Pass ONLY the current query to the config so partial indexes are built specifically for it
        let (index_name, config) = make_config_from_env(vec![query_string.clone()]);
        
        println!("\n==========================================================");
        println!("INDEX_TYPE = {} | QUERY = {}", index_name, query_id);
        println!("WINDOW_SIZE = {} | SLIDE_SIZE = {}", window_size, slide_size);
        println!("==========================================================");

        // Initialize a fresh database for this query run
        let mut db = SparqlDatabase::with_config(config);
        let mut current_window = Vec::new();

        let mut total_insertion_time = 0.0;
        let mut total_deletion_time = 0.0;
        let mut total_query_time = 0.0;
        let mut window_count = 0;
        let mut first = true;

        for chunk in all_triples.chunks(slide_size) {
            // --- SLIDE IN ---
            let insert_start = Instant::now();
            let batch_data = chunk.join("\n");
            db.parse_ntriples_and_add(&batch_data);
            
            // Indexes have to be built on first window
            if first {
                db.get_or_build_stats();
                db.build_all_indexes();
                first = false;
            }
            
            current_window.extend_from_slice(chunk);
            total_insertion_time += insert_start.elapsed().as_secs_f64();

            // --- SLIDE OUT (Manual Window Management) ---
            if current_window.len() > window_size {
                let overflow = current_window.len() - window_size;
                let to_remove: Vec<String> = current_window.drain(0..overflow).collect();
                let batch_delete_data = to_remove.join("\n");

                let delete_start = Instant::now();
                db.parse_ntriples_and_remove(&batch_delete_data); 
                total_deletion_time += delete_start.elapsed().as_secs_f64();
            }

            // --- EXECUTE ONLY CURRENT QUERY ---
            let query_start = Instant::now();
            let _results = execute_query_rayon_parallel2_volcano(&query_string, &mut db);
            total_query_time += query_start.elapsed().as_secs_f64();
            
            window_count += 1;
            
            if window_count % 5 == 0 {
                println!("  Window {} processed. Active Triples: {}", window_count, current_window.len());
            }
        }

        println!("\n--- Final Benchmark Results: {} on {} ---", query_id, index_name);
        println!("Total Window Slide Operations: {}", window_count);
        println!("Total Insertion Time: {:.4} s", total_insertion_time);
        println!("Total Deletion Time: {:.4} s", total_deletion_time);
        println!("Total Query Time: {:.4} s", total_query_time);
        
        let total_time = total_insertion_time + total_deletion_time + total_query_time;
        println!("Overall Throughput: {:.2} windows/sec", (window_count as f64) / total_time);
    }
}
